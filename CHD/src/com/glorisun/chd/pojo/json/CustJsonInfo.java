package com.glorisun.chd.pojo.json;

public class CustJsonInfo {
	
	private int custId;
	
	private String custName;
	
	private int custCompId;
	
	private int custState;
	
	private String custStateName;
	
	private String custType;
	
	private String entProp;
	
	public String getCustStateName() {
		return custStateName;
	}

	public void setCustStateName(String custStateName) {
		this.custStateName = custStateName;
	}

	private boolean custRegion;
	
	private String custCompName;

	public int getCustId() {
		return custId;
	}

	public void setCustId(int custId) {
		this.custId = custId;
	}

	public String getCustName() {
		return custName;
	}

	public void setCustName(String custName) {
		this.custName = custName;
	}

	public int getCustCompId() {
		return custCompId;
	}

	public void setCustCompId(int custCompId) {
		this.custCompId = custCompId;
	}

	public String getCustCompName() {
		return custCompName;
	}

	public void setCustCompName(String custCompName) {
		this.custCompName = custCompName;
	}

	public int getCustState() {
		return custState;
	}

	public void setCustState(int custState) {
		this.custState = custState;
	}

	public boolean isCustRegion() {
		return custRegion;
	}

	public void setCustRegion(boolean custRegion) {
		this.custRegion = custRegion;
	}

	public String getCustType() {
		return custType;
	}

	public void setCustType(String custType) {
		this.custType = custType;
	}

	public String getEntProp() {
		return entProp;
	}

	public void setEntProp(String entProp) {
		this.entProp = entProp;
	}

	
}
