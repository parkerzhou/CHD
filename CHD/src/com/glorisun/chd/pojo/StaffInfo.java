package com.glorisun.chd.pojo;

public class StaffInfo {
	
	private int id;
	
	private String staff_code;
	
	private String staff_name;
	
	private String dept_sname;
	
	private String tel_no;
	
	private String mobile_no;
	
	private String staff_email;
	
	private String staff_title;
	
	private String cmp_sName;

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getStaff_code() {
		return staff_code;
	}

	public void setStaff_code(String staff_code) {
		this.staff_code = staff_code;
	}

	public String getStaff_name() {
		return staff_name;
	}

	public void setStaff_name(String staff_name) {
		this.staff_name = staff_name;
	}

	public String getDept_sname() {
		return dept_sname;
	}

	public void setDept_sname(String dept_sname) {
		this.dept_sname = dept_sname;
	}

	public String getTel_no() {
		return tel_no;
	}

	public void setTel_no(String tel_no) {
		this.tel_no = tel_no;
	}

	public String getMobile_no() {
		return mobile_no;
	}

	public void setMobile_no(String mobile_no) {
		this.mobile_no = mobile_no;
	}

	public String getStaff_email() {
		return staff_email;
	}

	public void setStaff_email(String staff_email) {
		this.staff_email = staff_email;
	}

	public String getStaff_title() {
		return staff_title;
	}

	public void setStaff_title(String staff_title) {
		this.staff_title = staff_title;
	}

	public String getCmp_sName() {
		return cmp_sName;
	}

	public void setCmp_sName(String cmp_sName) {
		this.cmp_sName = cmp_sName;
	}

}
