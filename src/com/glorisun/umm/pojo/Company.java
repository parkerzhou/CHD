package com.glorisun.umm.pojo;

public class Company {
	private Integer cmp_id;
	private String cmp_code;
	private String cmp_name;
	public Integer getCmp_id() {
		return cmp_id;
	}
	public void setCmp_id(Integer cmp_id) {
		this.cmp_id = cmp_id;
	}
	public String getCmp_code() {
		return cmp_code;
	}
	public void setCmp_code(String cmp_code) {
		this.cmp_code = cmp_code;
	}
	public String getCmp_name() {
		return cmp_name;
	}
	public void setCmp_name(String cmp_name) {
		this.cmp_name = cmp_name;
	}
	
	
}
