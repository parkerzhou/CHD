package com.glorisun.umm.pojo;

public class UserState {
	private int st_id;
	private String state;//状态
	private String st_orderSeq;
	public int getSt_id() {
		return st_id;
	}
	public void setSt_id(int st_id) {
		this.st_id = st_id;
	}
	public String getState() {
		return state;
	}
	public void setState(String state) {
		this.state = state;
	}
	public String getSt_orderSeq() {
		return st_orderSeq;
	}
	public void setSt_orderSeq(String st_orderSeq) {
		this.st_orderSeq = st_orderSeq;
	}
}
