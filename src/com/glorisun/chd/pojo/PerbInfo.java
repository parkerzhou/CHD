package com.glorisun.chd.pojo;

public class PerbInfo {
	private String performance_seq;
	private String nick;
	private String frans_id;
	private String discontinue;
	private String canceled;
	private String return_money;
	public String getPerformance_seq() {
		return performance_seq;
	}
	public void setPerformance_seq(String performance_seq) {
		this.performance_seq = performance_seq;
	}
	public String getNick() {
		return nick;
	}
	public void setNick(String nick) {
		this.nick = nick;
	}
	public String getFrans_id() {
		return frans_id;
	}
	public void setFrans_id(String frans_id) {
		this.frans_id = frans_id;
	}
	public String getDiscontinue() {
		return discontinue;
	}
	public void setDiscontinue(String discontinue) {
		this.discontinue = discontinue;
	}
	public String getCanceled() {
		return canceled;
	}
	public void setCanceled(String canceled) {
		this.canceled = canceled;
	}
	public String getReturn_money() {
		return return_money;
	}
	public void setReturn_money(String return_money) {
		this.return_money = return_money;
	}
}
