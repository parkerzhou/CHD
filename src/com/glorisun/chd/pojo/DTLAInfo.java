package com.glorisun.chd.pojo;

public class DTLAInfo {
	private String performance_seq;
	private String rec_date;
	private String rec_money;
	public String getPerformance_seq() {
		return performance_seq;
	}
	public void setPerformance_seq(String performance_seq) {
		this.performance_seq = performance_seq;
	}
	public String getRec_date() {
		return rec_date;
	}
	public void setRec_date(String rec_date) {
		this.rec_date = rec_date;
	}
	public String getRec_money() {
		return rec_money;
	}
	public void setRec_money(String rec_money) {
		this.rec_money = rec_money;
	}
}
